// Helper function to get Polish plural form for credits
export function getCreditPlural(count: number): string {
  if (count === 1) return POLISH_CONTENT.credits.oneCredit;
  if (count >= 2 && count <= 4) return POLISH_CONTENT.credits.fewCredits;
  return POLISH_CONTENT.credits.manyCredits;
}

// Helper function to format credit amount with proper plural
export function formatPolishCredits(count: number): string {
  return `${count} ${getCreditPlural(count)}`;
}

// Helper function to format currency in Polish locale
export function formatPolishCurrency(amount: number): string {
  return new Intl.NumberFormat('pl-PL', {
    style: 'currency',
    currency: 'PLN'
  }).format(amount);
}

// Helper function to format date in Polish locale
export function formatPolishDate(date: string | Date): string {
  const dateObj = typeof date === 'string' ? new Date(date) : date;
  return new Intl.DateTimeFormat('pl-PL', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit'
  }).format(dateObj);
}

// Helper function to get relative time in Polish
export function getPolishRelativeTime(date: string | Date): string {
  const dateObj = typeof date === 'string' ? new Date(date) : date;
  const now = new Date();
  const diffInHours = Math.floor((now.getTime() - dateObj.getTime()) / (1000 * 60 * 60));
  
  if (diffInHours < 1) return 'Przed chwilą';
  if (diffInHours < 24) return `${diffInHours} godzin temu`;
  if (diffInHours < 48) return 'Wczoraj';
  if (diffInHours < 168) return `${Math.floor(diffInHours / 24)} dni temu`;
  
  return formatPolishDate(dateObj);
}